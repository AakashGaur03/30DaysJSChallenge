export const constVal = 5;

export const squareNum = (num) => {
    return num * num
}

export const cubeNum = (num) => {
    return num * num * num
}

export const addNumbers = (num1, num2) => {
    return num1 + num2
}

export const subtractNumbers = (num1, num2) => {
    return num1 - num2
}