const addTwoNumbers = (num1, num2) => {
    return num1 + num2
}
const sybTwoNumbers = (num1, num2) => {
    return num1 - num2
}
const mulTwoNumbers = (num1, num2) => {
    return num1 * num2
}
const divTwoNumbers = (num1, num2) => {
    return num1 / num2
}

export {
    addTwoNumbers,
    sybTwoNumbers,
    mulTwoNumbers,
    divTwoNumbers
}